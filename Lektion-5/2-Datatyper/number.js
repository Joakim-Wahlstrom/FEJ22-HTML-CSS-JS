/* 
  NUMBERS
  -----------------------------------------------------------------

  +   lägg till
  -   ta bort
  *   gångra
  **  upphöjt i
  /   dela
  ++  öka med 1
  --  minska med 1
  %   division remainder

  =   lika med
  +=  lägg till
  -=  ta bort från
  *=  gångra och tilldela
  /=  dela och tilldela
  %=  division remainder

*/


let nr1 = 10;
let nr2 = 2.5;

// console.log(nr1, nr2);

// console.log(nr1 + nr2);
// console.log(nr1 - nr2);

nr1 = nr1 + 1;
nr1 += 1;
nr1 ++;

nr1 = nr1 - 1;
nr1 -= 1;
nr1 --;


// console.log(nr1)
console.log(nr1 * nr2);
console.log(nr1 ** nr2);
console.log(nr1 % 4);

